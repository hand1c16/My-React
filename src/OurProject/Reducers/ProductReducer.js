const ProductReducer = (state = watchesState, action) => {
    switch(action.type){
        case 'ADD_QTY':
            return state.map(product => {
                if(product.id === action.productId){
                    product.qty = product.qty + 1
                }
                return product
            })
        case 'DECREASE_QTY':
            return state.map( product => {
                if(product.id === action.payload){
                    if(product.qty !== 0)
                        product.qty = product.qty - 1
                    }
        
                return product
            })
        default:
            return state
    }
}
export default ProductReducer;